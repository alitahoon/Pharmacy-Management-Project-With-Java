    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        try  {
            String strstatement = "select * from employee where";
            if (rdID.isSelected()) {
                strstatement += " empID";
            } else if (rdName.isSelected()) {
                strstatement += " empName";
            }
            strstatement += " like '%" +txtSearchKey.getText()+"%'";
            emp.getCustomRows(strstatement, tblEmployee);
        
        } catch (Exception e) {
            myprotools.msgbooks(e.getMessage());
        }

    }//GEN-LAST:event_btnSearchActionPerformed

