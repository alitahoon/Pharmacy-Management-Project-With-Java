    private void tblEmployeeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEmployeeMouseClicked
        try {
            int row = tblEmployee.getSelectedRow();
            String strID = tblEmployee.getValueAt(row, 0).toString();
            String strName = tblEmployee.getValueAt(row, 1).toString();
            String strStart = tblEmployee.getValueAt(row, 2).toString();
            String strEnd = tblEmployee.getValueAt(row, 3).toString();
            String strPhone = tblEmployee.getValueAt(row, 4).toString();
            String strSalary = tblEmployee.getValueAt(row, 5).toString();

            txtID.setText(strID);
            txtName.setText(strName);
            txtShiftStart.setText(strStart);
            txtShiftEnd.setText(strEnd);
            txtPhone.setText(strPhone);
            txtSalary.setText(strSalary);

            btnAdd.setEnabled(false);
            btnDelete.setEnabled(true);
            btnUpdate.setEnabled(true);
        } catch (Exception e) {
            myprotools.msgbooks(e.getMessage());
        }
    }//GEN-LAST:event_tblEmployeeMouseClicked

