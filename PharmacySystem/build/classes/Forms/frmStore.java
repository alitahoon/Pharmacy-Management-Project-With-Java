    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        try  {
            String strstatement = "select * from store where";
            if (rdCode.isSelected()) {
                strstatement += " storeCode";
            } else if (rdAddress.isSelected()) {
                strstatement += " storeAddress";
            }
            strstatement += " like '%" +txtStrSearchKey.getText()+"%'";
            str.getCustomRows(strstatement, tblStore);
        
        } catch (Exception e) {
            myprotools.msgbooks(e.getMessage());
        }
    }//GEN-LAST:event_btnSearchActionPerformed

