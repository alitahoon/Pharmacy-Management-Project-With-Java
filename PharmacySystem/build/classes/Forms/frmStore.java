    Store str=new Store();
    private void setValues(){
        str.setStrCode(Integer.parseInt(txtStrCode.getText()));
        str.setStrAddress(txtStrAddress.getText());
        str.setStrPhone(txtStrPhone.getText());
        
    }
    private void clearText(){
        myprotools.cleartxt(this);
        txtStrCode.setText(str.getAutoNumber());
        btnAdd.setEnabled(true);
        btnUpdate.setEnabled(false);
        btnDelete.setEnabled(false);
        txtStrAddress.requestFocus();
        str.getAllRows(tblStore);
    }
    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        str.getAllRows(tblStore);
        btnGroup.add(rdCode);
        btnGroup.add(rdAddress);
        clearText();
        txtStrCode.setText(str.getAutoNumber());
    }//GEN-LAST:event_formWindowOpened

    private void tblStoreMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStoreMouseClicked
        int row=tblStore.getSelectedRow();
        String code=tblStore.getValueAt(row, 0).toString();
        String address=tblStore.getValueAt(row, 1).toString();
        String phone=tblStore.getValueAt(row, 2).toString();
        
        txtStrCode.setText(code);
        txtStrAddress.setText(address);
        txtStrPhone.setText(phone);
        
        btnAdd.setEnabled(false);
        btnUpdate.setEnabled(true);
        btnDelete.setEnabled(true);
        
    }//GEN-LAST:event_tblStoreMouseClicked

